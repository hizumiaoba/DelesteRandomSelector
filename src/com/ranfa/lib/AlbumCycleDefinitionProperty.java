package com.ranfa.lib;

public class AlbumCycleDefinitionProperty {

	private String type;
	private int daysLeft;
	private String dateDefinited;

	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getDaysLeft() {
		return daysLeft;
	}
	public void setDaysLeft(int daysLeft) {
		this.daysLeft = daysLeft;
	}
	public String getDateDefinited() {
		return dateDefinited;
	}
	public void setDateDefinited(String dateDefinited) {
		this.dateDefinited = dateDefinited;
	}

}
